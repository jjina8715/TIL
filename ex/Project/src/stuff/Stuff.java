package stuff;

public class Stuff {
	private String id;
	private String name;
	private int price;
	
	public String getId() {
		return id;
	}
	public void setId(String i) {
		this.id = i;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String val) {
		this.name = val;
	}
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int val) {
		this.price = val;
	}
}
